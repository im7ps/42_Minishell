#include "../inc/libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	t_list	*tmp;

	tmp = lst;
	if (lst != NULL)
	{
		lst = tmp->next;	//andiamo al prossimo nodo
		del(tmp->content);	//eliminiamo il contenuto del nodo precedente
		free(tmp);			//SEMPRE FARE LA FREEEEEE
	}
}
